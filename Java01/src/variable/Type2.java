package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		/*
		 이름 : OOO
		 출생년도 : OOOO년
		 키 : OOO.OOcm
		 */
		
		String name ="홍길동";
		int bornY = 1999;
		double h = 182.5;
		
		System.out.println("이름 : "+name);
		System.out.println("출생년도 : "+bornY + "년");
		System.out.println("키 : " + h + "cm");
	}
}
